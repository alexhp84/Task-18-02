#{1, 2} ? {3, 4} = {1, 2, 3, 4}
print({1, 2} | {3, 4})

#{1, 2, 3} ? {3, 4} = {3}
print({1, 2, 3} & {3, 4})

#{1, 2, 3, 4} ? {3, 4} = {1, 2}
print({1, 2, 3, 4} - {3, 4})

#{1, 2, 3} ? {3, 4} = {1, 2, 4}
print({1, 2, 3} ^ {3, 4})

#{1, 2, 3, 4} ? {1, 2} = True
print({1, 2, 3, 4} >= {1, 2})

#{1, 2} ? {1, 2, 3, 4} = True
print({1, 2, 3} >= {1, 2})

#{5, 6} ? {6, 7} = {5, 6, 7}
print({5, 6} | {6, 7})

#{10, 11, 12} ? {12, 13} = {10, 11}
print({10, 11, 12} - {12, 13})
